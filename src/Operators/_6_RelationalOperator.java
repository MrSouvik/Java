package Operators;

public class _6_RelationalOperator {
	/*
	 * Types of Relational Operator -
	 * ----------------------------
	 * 1. Less than (<)
	 * 2. Grater than (>)
	 * 3. Less than or equal to (<=)
	 * 4. Grater than or equal to (>=)
	 * 5. Equal to (==)
	 * 6. Not equal to (!=)
	 */
	
	public static void main(String[] args) {
		int a = 3;
		int b = 4;
		// 1. Less than (<)
		System.out.println("(a<b) : "+(a<b));
		// 2. Grater than (>)
		System.out.println("(a>b) : "+(a>b));
		// 3. Less than or equal to (<=)
		System.out.println("(a<=b) : "+(a<=b));
	    // 4. Grater than or equal to (>=)
		System.out.println("(a>=b) : "+(a>=b));
		// 5. Equal to (==)
		System.out.println("(a==3) : "+(a==3));
		// 6. Not equal to (!=)
		System.out.println("(a!=b) : "+(a!=b));
	}

}
